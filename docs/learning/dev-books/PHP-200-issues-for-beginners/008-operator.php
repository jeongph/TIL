<?php

// 연산자는 각각 +, -, *, /, % 로, 다른 언어들과 역할은 동일

// 더하기
$sum = 5 + 5;
echo "5 + 5 = ".$sum;
echo "<br>";

// 빼기
$minus = 5 - 2;
echo "5 - 2 = ".$minus;
echo "<br>";

// 곱하기
$mul = 5 * 3;
echo "5 * 3 = ".$mul;
echo "<br>";

// 나누기
$division = 6 / 3;
echo "6 / 3 = ".$division;
echo "<br>";

// 나머지
$rest = 5 % 3;
echo "5 % 3 = ".$rest;
echo "<br>";

?>